public class FilterUtils {


    String f1, f2, f3, f4;

    public FilterUtils(String f1, String f2, String f3, String f4) {

        this.f1 = f1;
        this.f2 = f2;
        this.f3 = f3;
        this.f4 = f4;

    }


    public String getF1() {
        return f1;
    }

    public void setF1(String f1) {
        this.f1 = f1;
    }

    public String getF2() {
        return f2;
    }

    public void setF2(String f2) {
        this.f2 = f2;
    }

    public String getF3() {
        return f3;
    }

    public void setF3(String f3) {
        this.f3 = f3;
    }

    public String getF4() {
        return f4;
    }

    public void setF4(String f4) {
        this.f4 = f4;
    }




}
